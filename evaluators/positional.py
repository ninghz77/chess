def _mirror(square: int) -> int:
    """Vertically mirror a square index (flip rank)."""
    return (7 - square // 8) * 8 + (square % 8)


class PositionalEvaluator(EvaluatorBase):
    """
    Material count plus piece-square table bonuses.

    Significantly stronger than the plain material evaluator because it
    rewards good piece placement (central pawns, active knights, etc.).
    """

    name = 'positional'

    def evaluate(self, game_state) -> int:
        score = 0
        for s in range(64):
            piece = game_state.board.squares[s]
            if piece is None:
                continue
            color, pt = piece
            material = self.piece_value(pt)
            table = _PST.get(pt)
            if table is not None:
                positional = table[s] if color == Color.WHITE else table[_mirror(s)]
            else:
                positional = 0
            value = material + positional
            score += value if color == Color.WHITE else -value
        return score
